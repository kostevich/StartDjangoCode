#==========================================================================================#
# >>>>> СТРАНИЦА REVERSED TEXT <<<<< #
#==========================================================================================#

def reverse(request):
    # Текст пользователя.
    user_text = request.GET['user']

    # Переворот текста.
    reverse = user_text[::-1]

    return render(request, 'Reverse.html', {'word': reverse})